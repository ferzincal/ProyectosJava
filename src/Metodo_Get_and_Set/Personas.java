package Metodo_Get_and_Set;

/**
 *
 * @author ferzincal
 */
public class Personas {
    // Atributos
    String nombrePersona;
    int edadPersona;
    Float estaturaPersona;
    char generoPersona;
    double pesoPersona;
    boolean solteroPersona;
    
    //Metodos Get y Set
    public String getNombrePersona() {
        return nombrePersona;
    }

    public void setNombrePersona(String nombrePersona) {
        this.nombrePersona = nombrePersona;
    }

    public int getEdadPersona() {
        return edadPersona;
    }

    public void setEdadPersona(int edadPersona) {
        this.edadPersona = edadPersona;
    }

    public Float getEstaturaPersona() {
        return estaturaPersona;
    }

    public void setEstaturaPersona(Float estaturaPersona) {
        this.estaturaPersona = estaturaPersona;
    }

    public char getGeneroPersona() {
        return generoPersona;
    }

    public void setGeneroPersona(char generoPersona) {
        this.generoPersona = generoPersona;
    }

    public double getPesoPersona() {
        return pesoPersona;
    }

    public void setPesoPersona(double pesoPersona) {
        this.pesoPersona = pesoPersona;
    }

    public boolean isSolteroPersona() {
        return solteroPersona;
    }

    public void setSolteroPersona(boolean solteroPersona) {
        this.solteroPersona = solteroPersona;
    }
}
    
 